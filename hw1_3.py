## Ваши списки
list1 = [3, 12, 8, 41, 7, 21, 9, 14, 5, 30]
list2 = [9, 5, 6, 12, 14, 22, 17, 41, 8, 3]

# Объединение двух списков и преобразование во множество для удаления дубликатов
unique_numbers = set(list1 + list2)

# Подсчет количества уникальных чисел
count_unique = len(unique_numbers)

# Вывод результата
print(f"Количество уникальных чисел: {count_unique}")


#Идеальное решение
count = 0  # Инициализируем счетчик совпадающих чисел

# Перебираем элементы первого списка
for num1 in list1:
    # Перебираем элементы второго списка
    for num2 in list2:
        # Если числа совпадают, увеличиваем счетчик
        if num1 == num2:
            count += 1

print("Количество совпадающих чисел:", count)